import math

def addition_ceil(num1,num2):
    result=num1+num2
    return math.ceil(result)

def subtraction_ceil(num1,num2):
    result=num1-num2
    return math.ceil(result)

def multiplication_ceil(num1,num2):
    result=num1*num2
    return math.ceil(result)

def division_ceil(num1,num2):
    result=num1/num2
    return math.ceil(result)